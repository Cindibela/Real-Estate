Fair Housing: It's not just a nice thing to talk about, it's the law!
Laws That Protect You
<li>Federal and state fair housing laws were put into effect to create an even playing field for homebuyers in all areas of a real estate transaction. These laws prohibit discrimination based on race, color, religion, sex, disability, familial status, and national origin.</li>
<h6>Civil Rights Act of 1866</h6>
<li>The federal Civil Rights Act of 1866 prohibits all racial discrimination in the sale or rental of property.</li>
<h6>Civil Rights Act of 1968 and 1988 Amendment</h6>
<li>In leasing or selling residential property, the Civil Rights Act of 1968 expands the definition of discrimination to include not only race, but also national origin, color, and religion. The Fair Housing Amendments Act of 1988 further broadens the definition to include age, sex, and handicapped status.</li>

<h6>Fair Housing Act</h6>
<li>The federal Fair Housing Act of 1988 and Title VIII of the Civil Rights Act of 1968 constitute the Fair Housing Act. The Act makes fair housing a national policy throughout the U.S. It prohibits discrimination in the sale, lease or rental of housing, or making housing otherwise unavailable because of race, color, religion, sex, disability, familial status or national origin.</li>

<h6>Americans with Disabilities Act</h6>
<li>Title III of the federal Americans with Disabilities Act prohibits discrimination against persons with disabilities in commercial facilities and places of public accommodation.</li>

<h6>Equal Credit Opportunity Act</h6>
<li>The federal Equal Credit Opportunity Act makes it unlawful to discriminate against anyone on a credit application due to race, color, religion, national origin, sex, marital status, age or because all or part of an applicant's income comes from any public assistance program.</li>

<h6>Know Your Rights and Responsibilities</h6>
<li>Homesellers, prospective homebuyers, real estate agents, mortgage brokers and loan officers all have rights and responsibilities under the law.</li>

<h6>Sellers' Responsibilities</h6>
<li>As a home seller or landlord, you are obligated not to discriminate in the sale, rental or financing of your property on the basis of race, color, religion, sex, disability, familial status or national origin. Also, you cannot do so through your licensed broker or salesperson, who is also bound by anti-discrimination laws. You may not set any discriminatory terms or conditions in a purchase contract or a lease. Additionally, you may not deny that housing is available or advertise a property's availability only to persons of a certain race, color, religion, sex, disability, familial status or national origin.</li>

<h6>Real Estate Professionals' Responsibilities</h6>
<li>Real Estate agents, mortgage brokers and loan officers in a real estate transaction may not discriminate on the basis of race, color, religion, sex, disability, familial status or national origin. Equally as important, they may not follow such instructions from a homeseller or landlord.</li>

<h6>What To Do if You Feel the Law Has Been Violated</h6>
<li>Discrimination complaints about housing may be filed with the nearest office of the U.S. Dept. of Housing and Urban Development (HUD) or by calling HUD's telephone numbers, (202)708-1112 (Voice) or (202)708-1455 (TTY). Or contact HUD on the Internet at http://www.hud.gov/fhe/fheo.html.</li>

